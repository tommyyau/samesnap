// Flatten all emojis for quick access
export const ALL_EMOJIS: EmojiItem[] = Object.values(EMOJI_DATA).flatMap(cat => cat.emojis);

// Create a lookup map for fast name search
export const EMOJI_BY_CHAR: Map<string, EmojiItem> = new Map(
  ALL_EMOJIS.map(emoji => [emoji.char, emoji])
);

// Helper to get emoji name by character
export function getEmojiName(char: string): string | undefined {
  return EMOJI_BY_CHAR.get(char)?.name;
}

// Helper to search emojis by name
export function searchEmojis(query: string): EmojiItem[] {
  const lowerQuery = query.toLowerCase();
  return ALL_EMOJIS.filter(emoji =>
    emoji.name.toLowerCase().includes(lowerQuery) ||
    emoji.char.includes(query)
  );
}
